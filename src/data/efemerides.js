// Derivado: solo las fechas marcadas como feriado real (para el cálculo de
// findes largos). Mantiene la forma { date, name, type } que ya usaba
// computeFindesLargos.
export const FERIADOS_2026 = EFEMERIDES_2026
  .filter(e => e.feriado)
  .map(e => ({ date: e.date, name: e.name, type: 'feriado' }))

const PRIORIDAD_ORDEN = { Alta: 0, Media: 1, Baja: 2 }

// ── Helpers ──────────────────────────────────────────────────────────

function toDate(iso) {
  const [y, m, d] = iso.split('-').map(Number)
  return new Date(Date.UTC(y, m - 1, d))
}
function toISO(d) {
  return d.toISOString().slice(0, 10)
}
function addDays(d, n) {
  const nd = new Date(d)
  nd.setUTCDate(nd.getUTCDate() + n)
  return nd
}
function isWeekend(d) {
  const day = d.getUTCDay()
  return day === 0 || day === 6
}

/**
 * Calcula los fines de semana largos del año a partir de la lista de
 * feriados: agrupa corridas de 3+ días no laborables consecutivos
 * (fines de semana + feriados/puentes pegados) que incluyan al menos
 * un feriado real (para no marcar un finde de semana común).
 */
export function computeFindesLargos(feriados = FERIADOS_2026) {
  const feriadoDates = new Map(feriados.map(f => [f.date, f.name]))
  if (!feriados.length) return []

  const allDates = feriados.map(f => toDate(f.date))
  const minDate = addDays(new Date(Math.min(...allDates)), -4)
  const maxDate = addDays(new Date(Math.max(...allDates)), 4)

  const nonWorking = new Set()
  for (let d = new Date(minDate); d <= maxDate; d = addDays(d, 1)) {
    if (isWeekend(d) || feriadoDates.has(toISO(d))) nonWorking.add(toISO(d))
  }

  const findes = []
  let runStart = null
  let prevISO = null
  const sortedDates = [...nonWorking].sort()

  const flush = (endISO) => {
    if (!runStart) return
    const startD = toDate(runStart)
    const endD = toDate(endISO)
    const days = Math.round((endD - startD) / 86400000) + 1
    const motivos = feriados
      .filter(f => f.date >= runStart && f.date <= endISO)
      .map(f => f.name)
    if (days >= 3 && motivos.length) {
      findes.push({ start: runStart, end: endISO, days, motivos: [...new Set(motivos)] })
    }
    runStart = null
  }

  for (const iso of sortedDates) {
    if (!runStart) { runStart = iso; prevISO = iso; continue }
    const gap = (toDate(iso) - toDate(prevISO)) / 86400000
    if (gap === 1) {
      prevISO = iso
    } else {
      flush(prevISO)
      runStart = iso
      prevISO = iso
    }
  }
  flush(prevISO)

  return findes
}

export const FINDES_LARGOS_2026 = computeFindesLargos(FERIADOS_2026)

/**
 * Devuelve el próximo finde largo que empieza dentro de los próximos
 * `withinDays` días (contando desde `fromISO`, default hoy). Sirve para
 * el aviso "se viene un finde largo, generá contenido" en el calendario.
 */
export function getUpcomingFindeLargo(fromISO = toISO(new Date()), withinDays = 12) {
  const from = toDate(fromISO)
  const limit = addDays(from, withinDays)
  return FINDES_LARGOS_2026.find(f => {
    const start = toDate(f.start)
    return start >= from && start <= limit
  }) || null
}

/**
 * Todas las efemérides de una fecha YYYY-MM-DD puntual (puede haber más de
 * una el mismo día — ej. 20/07 tiene feriado del Mundial + Día del Amigo).
 * Devuelve un array, ordenado por prioridad (Alta primero).
 */
export function getEfemerideFor(iso) {
  return EFEMERIDES_2026
    .filter(e => e.date === iso)
    .sort((a, b) => (PRIORIDAD_ORDEN[a.prioridad] ?? 9) - (PRIORIDAD_ORDEN[b.prioridad] ?? 9))
}

/**
 * Efemérides del mes SIGUIENTE al de `fromISO` (default hoy) — para armar
 * el aviso de fin de mes ("se viene tal mes, planificá esto"). Ordenadas
 * por fecha, con las de prioridad Alta/Media primero dentro de cada fecha.
 */
export function getNextMonthEfemerides(fromISO = toISO(new Date())) {
  const from = toDate(fromISO)
  const nextMonthIdx = (from.getUTCMonth() + 1) % 12
  const nextMonthYear = from.getUTCMonth() === 11 ? from.getUTCFullYear() + 1 : from.getUTCFullYear()
  return EFEMERIDES_2026
    .filter(e => {
      const d = toDate(e.date)
      return d.getUTCMonth() === nextMonthIdx && d.getUTCFullYear() === nextMonthYear
    })
    .sort((a, b) => a.date === b.date
      ? (PRIORIDAD_ORDEN[a.prioridad] ?? 9) - (PRIORIDAD_ORDEN[b.prioridad] ?? 9)
      : a.date.localeCompare(b.date))
}

/**
 * True si `fromISO` cae dentro de los últimos `daysBefore` días del mes
 * (incluyendo el último día) — la ventana en la que avisamos qué se viene
 * el mes que arranca, para no depender de acertar el día exacto.
 */
export function isNearMonthEnd(fromISO = toISO(new Date()), daysBefore = 3) {
  const from = toDate(fromISO)
  const lastDay = new Date(Date.UTC(from.getUTCFullYear(), from.getUTCMonth() + 1, 0))
  const diff = Math.round((lastDay - from) / 86400000)
  return diff >= 0 && diff < daysBefore
}

const MES_NOMBRES = ['enero', 'febrero', 'marzo', 'abril', 'mayo', 'junio', 'julio', 'agosto', 'septiembre', 'octubre', 'noviembre', 'diciembre']

/** Nombre del mes siguiente a `fromISO`, en minúsculas (para textos del aviso). */
export function nextMonthName(fromISO = toISO(new Date())) {
  const from = toDate(fromISO)
  const nextMonthIdx = (from.getUTCMonth() + 1) % 12
  return MES_NOMBRES[nextMonthIdx]
}
